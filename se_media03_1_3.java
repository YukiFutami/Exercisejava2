public class se_media03_1_3 {
    public static void main(String[] args){

        // 二重の for 文を用いて以下の出力を行ってください。
        // (10 列×5 行の*を出力)

        for(int i = 0; i <= 5; i++){
            for(int j = 0; j < 10; j++){
                System.out.print("*");

            }
            System.out.println();
        }

    }
}
