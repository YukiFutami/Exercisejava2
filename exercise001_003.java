public class exercise001_003 {
    public static void main(String[] args){

        //変数（変数宣言)　問題003
        // 定数を宣言して、それに値を代入してください。
        // 定数は変更不可で、一度代入されたら変更できないものです。
        // 例えば、”final double PI = 3.14159;” のように円周率の定数を宣言します。

        final double PI = 3.14159; // finalを使うときは変数名は大文字を使用する
        System.out.println(PI);
        // エラーメッセージなどに使うとよい



    }
}
